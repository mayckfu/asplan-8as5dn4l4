import { useMemo, useEffect, useState } from 'react'
import { useNavigate } from 'react-router-dom'
import { ArrowLeft, Loader2 } from 'lucide-react'
import { Button } from '@/components/ui/button'
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from '@/components/ui/table'
import { Amendment } from '@/lib/mock-data'
import { formatCurrencyBRL } from '@/lib/utils'
import { StatusBadge } from '@/components/StatusBadge'
import { supabase } from '@/lib/supabase/client'

const PropostasMacPage = () => {
  const navigate = useNavigate()
  const [macAmendments, setMacAmendments] = useState<Amendment[]>([])
  const [isLoading, setIsLoading] = useState(true)

  const [selectedYear] = useState(() => {
    return (
      localStorage.getItem('asplan_dashboard_year') ||
      new Date().getFullYear().toString()
    )
  })

  useEffect(() => {
    const fetchMacAmendments = async () => {
      setIsLoading(true)
      try {
        const { data, error } = await supabase
          .from('emendas')
          .select('*')
          .in('tipo_recurso', ['INCREMENTO_MAC', 'CUSTEIO_MAC'])
          .eq('ano_exercicio', parseInt(selectedYear))
          .order('created_at', { ascending: false })

        if (error) throw error

        setMacAmendments(data as Amendment[])
      } catch (error) {
        console.error('Error fetching MAC amendments:', error)
      } finally {
        setIsLoading(false)
      }
    }

    fetchMacAmendments()
  }, [selectedYear])

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-[calc(100vh-100px)]">
        <Loader2 className="h-8 w-8 animate-spin text-primary" />
      </div>
    )
  }

  return (
    <div className="space-y-6">
      <div className="flex items-center gap-4">
        <Button
          variant="outline"
          size="icon"
          className="h-8 w-8"
          onClick={() => navigate(-1)}
        >
          <ArrowLeft className="h-4 w-4" />
          <span className="sr-only">Voltar</span>
        </Button>
        <h1 className="flex-1 shrink-0 whitespace-nowrap text-xl font-semibold tracking-tight sm:grow-0">
          Propostas MAC — {selectedYear}
        </h1>
      </div>
      <Card>
        <CardHeader>
          <CardTitle>Lista de Propostas ({macAmendments.length})</CardTitle>
        </CardHeader>
        <CardContent>
          <Table>
            <TableHeader>
              <TableRow>
                <TableHead>Autor</TableHead>
                <TableHead>Nº Emenda</TableHead>
                <TableHead>Nº Proposta</TableHead>
                <TableHead className="text-right">Valor Total</TableHead>
                <TableHead>Situação Oficial</TableHead>
                <TableHead>Status Interno</TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {macAmendments.length === 0 ? (
                <TableRow>
                  <TableCell colSpan={6} className="text-center py-8">
                    Nenhuma proposta encontrada para o ano de {selectedYear}.
                  </TableCell>
                </TableRow>
              ) : (
                macAmendments.map((amendment) => (
                  <TableRow
                    key={amendment.id}
                    className="cursor-pointer"
                    onClick={() => navigate(`/emenda/${amendment.id}`)}
                  >
                    <TableCell>{amendment.autor}</TableCell>
                    <TableCell>{amendment.numero_emenda}</TableCell>
                    <TableCell>{amendment.numero_proposta}</TableCell>
                    <TableCell className="text-right tabular-nums">
                      {formatCurrencyBRL(amendment.valor_total)}
                    </TableCell>
                    <TableCell>
                      <StatusBadge status={amendment.situacao} />
                    </TableCell>
                    <TableCell>
                      <StatusBadge status={amendment.status_interno} />
                    </TableCell>
                  </TableRow>
                ))
              )}
            </TableBody>
          </Table>
        </CardContent>
      </Card>
    </div>
  )
}

export default PropostasMacPage
